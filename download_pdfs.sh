TOTAL=${#IDS[@]}
COUNT=0
FAILED=0

for ID in "${IDS[@]}"; do
    COUNT=$((COUNT + 1))
    OUTFILE="$PDF_DIR/vortrag_${ID}.pdf"
    if [ -f "$OUTFILE" ]; then
        echo "[$COUNT/$TOTAL] Skipping $ID (already exists)"
        continue
    fi
    echo "[$COUNT/$TOTAL] Downloading vortrag $ID..."
    if curl -sS -f -L -o "$OUTFILE" "https://ganglion.ch/html/php/download_vortrag.php?id=${ID}&download=pdf" 2>/dev/null; then
        # Check if it's actually a PDF
        if file "$OUTFILE" | grep -q PDF; then
            SIZE=$(stat -c%s "$OUTFILE" 2>/dev/null || stat -f%z "$OUTFILE" 2>/dev/null)
            echo "  -> OK (${SIZE} bytes)"
        else
            echo "  -> Not a PDF, removing"
            rm "$OUTFILE"
            FAILED=$((FAILED + 1))
        fi
    else
        echo "  -> FAILED"
        rm -f "$OUTFILE"
        FAILED=$((FAILED + 1))
    fi
    # Be polite to the server
    sleep 0.5
done

echo ""
echo "Done. Downloaded $((COUNT - FAILED)) PDFs, $FAILED failed."
